int	ft_atoi(const char *str)
{
	int	rs;
	int	n;
	int	s;

	s = 1;
	while (*str == 32 || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			s *= -1; 
		str++;
	}
	rs = 0;
	while (*str >= '0' && *str <= '9')
	{
		n = *str - '0';
		rs = rs * 10 + n;
		str++;
	}
	return (rs * s);
}
